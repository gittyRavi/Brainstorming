Its appearance is that of a typical sea turtle. C. mydas has a dorsoventrally flattened body, a beaked head at the end of a short neck, and paddle-like arms well-adapted for swimming.[22] Adult green turtles grow to 1.5 metres (5 ft) long.[23] The average weight of mature individuals is 68–190 kg (150–419 lb) and the average carapace length is 78–112 cm (31–44 in).[24] Exceptional specimens can weigh 315 kg (694 lb) or even more, with the largest known C. mydas having weighed 395 kg (871 lb) and measured 153 cm (60 in) in carapace length.[25]
Anatomically, a few characteristics distinguish the green turtle from the other members of its family. Unlike the closely related hawksbill turtle, the green turtle's snout is very short and its beak is unhooked. The neck cannot be pulled into the shell.[26] The sheath of the turtle's upper jaw possesses a denticulated edge, while its lower jaw has stronger, serrated, more defined denticulation. The dorsal surface of the turtle's head has a single pair of prefrontal scales. Its carapace is composed of five central scutes flanked by four pairs of lateral scutes. Underneath, the green turtle has four pairs of inframarginal scutes covering the area between the turtle's plastron and its shell. Mature C. mydas front appendages have only a single claw (as opposed to the hawksbill's two), although a second claw is sometimes prominent in young specimens.[27]
The carapace of the turtle has various color patterns that change over time. Hatchlings of Chelonia mydas, like those of other marine turtles, have mostly black carapaces and light-colored plastrons. Carapaces of juveniles turn dark brown to olive, while those of mature adults are either entirely brown, spotted or marbled with variegated rays. Underneath, the turtle's plastron is hued yellow. C. mydas limbs are dark-colored and lined with yellow, and are usually marked with a large dark brown spot in the center of each appendage.[5][28]


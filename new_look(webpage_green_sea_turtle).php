<div style="background-color:palegreen;padding:5px;border-radius:5px;overflow:hidden"><div style="background-color:orange;padding:5px;border-radius:5px;overflow:hidden"></div><textarea maxlength="50" rows="7" cols="50" style="position:relative;max-width:100%">For the fictional superhero, see The Green Turtle.

The green sea turtle (Chelonia mydas), also known as the green turtle, black (sea) turtle, or Pacific green turtle,[3] is a large sea turtle of the family Cheloniidae. It is the only species in the genus Chelonia.[4] Its range extends throughout tropical and subtropical seas around the world, with two distinct populations in the Atlantic and Pacific Oceans, but it is also found in the Indian Ocean.[5][6] The common name comes from the usually green fat found beneath its carapace; these turtles' shells are olive to black.

This sea turtle's dorsoventrally flattened body is covered by a large, teardrop-shaped carapace; it has a pair of large, paddle-like flippers. It is usually lightly colored, although in the eastern Pacific populations parts of the carapace can be almost black. Unlike other members of its family, such as the hawksbill sea turtle, C. mydas is mostly herbivorous. The adults usually inhabit shallow lagoons, feeding mostly on various species of seagrasses.[7] The turtles bite off the tips of the blades of seagrass, which keeps the grass healthy.
Like other sea turtles, green sea turtles migrate long distances between feeding grounds and hatching beaches. Many islands worldwide are known as Turtle Island due to green sea turtles nesting on their beaches. Females crawl out on beaches, dig nests and lay eggs during the night. Later, hatchlings emerge and scramble into the water. Those that reach maturity may live to eighty years in the wild.[5]
C. mydas is listed as endangered by the IUCN and CITES and is protected from exploitation in most countries.[8] It is illegal to collect, harm or kill them. In addition, many countries have laws and ordinances to protect nesting areas. However, turtles are still in danger due to human activity. In some countries, turtles and their eggs are hunted for food. Pollution indirectly harms turtles at both population and individual scales. Many turtles die after being caught in fishing nets. Also, real estate development often causes habitat loss by eliminating nesting beaches.

Taxonomy[edit]
The green sea turtle is a member of the tribe Chelonini. A 1993 study clarified the status of genus Chelonia with respect to the other marine turtles. The carnivorous Eretmochelys (hawksbill), Caretta (loggerhead) and Lepidochelys (Ridley) were assigned to the tribe Carettini. Herbivorous Chelonia warranted their status as a genus, while Natator (flatback) was further removed from the other genera than previously believed.[9]
The species was originally described by Linnaeus in 1758 as Testudo mydas.[10] In 1868, Marie Firmin Bocourt named a particular species of sea turtle Chelonia agassizii.[11][12] This "species" was referred to as the "black sea turtle".[13] Later research determined Bocourt's "black sea turtle" was not genetically distinct from C. mydas, and thus taxonomically not a separate species.[14] These two "species" were then united as Chelonia mydas and populations were given subspecies status: C. mydas mydas referred to the originally described population, while C. mydas agassizi referred only to the Pacific population known as the Galápagos green turtle.[15][16] This subdivision was later determined to be invalid and all species members were then designated Chelonia mydas.[4] The oft-mentioned name C. agassizi remains an invalid junior synonym of C. mydas. Other authorities claim that the two populations are phenotypically distinct and treat the East Pacific form as a full species.[17]
The species' common name does not derive from any particular green external coloration of the turtle. Its name comes from the greenish color of the turtles' fat, which is only found in a layer between their inner organs and their shell.[18] As a species found worldwide, the green turtle is called differently in some languages and dialects. In the Hawaiian language, honu[19] is used to refer to this species.[20] In Hawaii, the green sea turtle is referred to as “honu” and is a symbol of good luck and longevity.[21]


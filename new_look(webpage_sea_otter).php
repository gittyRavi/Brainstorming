Taxonomy
The first scientific description of the sea otter is contained in the field notes of Georg Steller from 1751, and the species was described by Linnaeus in his Systema Naturae of 1758.[5] Originally named Lutra marina, it underwent numerous name changes before being accepted as Enhydra lutris in 1922.[6] The generic name Enhydra, derives from the Ancient Greek en/εν "in" and hydra/ύδρα "water",[7] meaning "in the water", and the Latin word lutris, meaning "otter".[8]
The sea otter was formerly sometimes referred to as the "sea beaver",[9] being the marine fur-bearer similar in commercial value to the terrestrial beaver. Rodents (of which the beaver is one) are not closely related to otters, which are carnivores. It is not to be confused with the marine otter, a rare otter species native to the southern west coast of South America. A number of other otter species, while predominantly living in fresh water, are commonly found in marine coastal habitats. The extinct sea mink of northeast North America is another mustelid that had adapted to a marine environment.

Evolution[edit]
The sea otter is the heaviest (the giant otter is longer, but significantly slimmer) member of the family Mustelidae,[10] a diverse group that includes the 13 otter species and terrestrial animals such as weasels, badgers, and minks. It is unique among the mustelids in not making dens or burrows, in having no functional anal scent glands,[11] and in being able to live its entire life without leaving the water.[12] The only member of the genus Enhydra, the sea otter is so different from other mustelid species that, as recently as 1982, some scientists believed it was more closely related to the earless seals.[13] Genetic analysis indicates the sea otter and its closest extant relatives, which include the African speckle-throated otter, European otter, African clawless otter and oriental small-clawed otter, shared an ancestor approximately 5 million years ago (Mya).[14]
Fossil evidence indicates the Enhydra lineage became isolated in the North Pacific approximately 2 Mya, giving rise to the now-extinct Enhydra macrodonta and the modern sea otter, Enhydra lutris.[6] The sea otter evolved initially in northern Hokkaidō and Russia, and then spread east to the Aleutian Islands, mainland Alaska, and down the North American coast.[15] In comparison to cetaceans, sirenians, and pinnipeds, which entered the water approximately 50, 40, and 20 Mya, respectively, the sea otter is a relative newcomer to a marine existence.[16] In some respects, though, the sea otter is more fully adapted to water than pinnipeds, which must haul out on land or ice to give birth.[17]


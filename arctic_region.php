Ten Facts about the Arctic!

Wrap up warm, gang, because we're off an icy adventure. Check out these ten brrr-illiant facts about the Arctic!

1. The Arctic is located at the northernmost part of our planet. Scientists usually define the Arctic as the area above the 'Arctic Circle' — an imaginary line that circles around the top of the globe. 

2. The Arctic consists of the Arctic Ocean and parts of Canada, Russia, the USA, Greenland, Norway, Finland, Sweden and Iceland. 

3. Because of the Earth’s tilt, for at least one day a year there’s an entire day of darkness in this freezing region — and also a full day of sunshine. Imagine that!

4. Temperatures as low as -70°C have been recorded in northern Greenland. Brrrrr!

5. Despite the freezing-cold temperatures, approximately four million people call this wintery wonderland home! Amongst these are the indigenous people of the Arctic, called the 'Inuits'. They’ve found ingenious ways to survive in one of the harshest environments on our planet. 

6. In 1958, a submarine called the USS Nautilus sailed beneath the frozen ice of the Arctic Ocean. This was proof that the enormous ice sheet rests on water and not land.

7. The Arctic is home to lots of wonderful wildlife, including polar bears, Arctic foxes, walruses, seals and whales!

8. One fascinating species found only in the Arctic is the narwhal, often referred to as the ‘unicorn of the sea’. Why? Well, male narwhals have a straight tusk projecting from the front of their head that can grow to over 3m in length!

9. The word 'Arctic' comes from the Greek word for bear, Arktos. But this isn’t because of the polar bears! It’s believed the name refers to two constellations that can be seen in the northern sky — ‘Ursa Minor’ (Little Bear) and ‘Ursa Major’ (Great Bear).

10. The ice of the Arctic contains around ten percent of the world’s fresh water. This giant, white, frozen reservoir reflects sunlight, helping keep the region cool. It also plays a super-important role in keeping our global climate stable.  

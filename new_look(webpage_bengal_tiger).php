In 1982, a sub-fossil right middle phalanx was found in a prehistoric midden near Kuruwita in Sri Lanka, which is dated to about 16,500 ybp and tentatively considered to be of a tiger. Tigers appear to have arrived in Sri Lanka during a pluvial period during which sea levels were depressed, evidently prior to the last glacial maximum about 20,000 years ago.[29] In 1929, the British taxonomist Pocock assumed that tigers arrived in southern India too late to colonize Sri Lanka, which earlier had been connected to India by a land bridge.[10]
In the Indian subcontinent, tigers inhabit tropical moist evergreen forests, tropical dry forests, tropical and subtropical moist deciduous forests, mangroves, subtropical and temperate upland forests, and alluvial grasslands. Latter tiger habitat once covered a huge swath of grassland and riverine and moist semi-deciduous forests along the major river system of the Gangetic and Brahmaputra plains, but has now been largely converted to agricultural land or severely degraded. Today, the best examples of this habitat type are limited to a few blocks at the base of the outer foothills of the Himalayas including the Tiger Conservation Units (TCUs) Rajaji-Corbett, Bardia-Banke, and the transboundary TCUs Chitwan-Parsa-Valmiki, Dudhwa-Kailali and Sukla Phanta-Kishanpur. Tiger densities in these TCUs are high, in part because of the extraordinary biomass of ungulate prey.[30]
The Bengal tigers in the Sundarbans in India and Bangladesh are the only tigers in the world inhabiting mangrove forests. The population in the Indian Sundarbans is estimated as 70 tigers in total.[2]


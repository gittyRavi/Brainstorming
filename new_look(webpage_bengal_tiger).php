<div style="background-color:grey;padding:5px;border-radius:5px;overflow:hidden"><div style="background-color:orange;padding:5px;border-radius:5px;overflow:hidden"></div><textarea maxlength="50" rows="7" cols="50" style="position:relative;max-width:100%">For other uses of 'Bengal tiger' and related terms, see Bengal tiger (disambiguation). For other uses of 'Royal Bengal tiger' and related terms, see Royal Bengal tiger (disambiguation).

The Bengal tiger (Panthera tigris tigris) is the most numerous tiger subspecies. By 2011, the total population was estimated at fewer than 2,500 individuals with a decreasing trend. None of the 'Tiger Conservation Landscapes' within the Bengal tiger's range is considered large enough to support an effective population size of 250 adult individuals. Since 2010, it is listed as Endangered on the IUCN Red List.[1]

As of 2010, Bengal tiger populations in India have been estimated at 1,706–1,909.[2] As of 2014, they had reputedly increased to an estimated 2,226 individuals,[3] but the method used in the census may not be accurate.[4][5]
Bengal tigers number around 440 in Bangladesh and 163–253 in Nepal.[6][7][8] Prior censuses placed the population of tigers in Bhutan at around 65-75 individuals, however, the latest census estimated that 103 wild Bengal tigers are living in the country.[9]
Bengal is traditionally fixed as the typical locality for the binomen Panthera tigris, to which the British taxonomist Reginald Innes Pocock subordinated the Bengal tiger in 1929 under the trinomen Panthera tigris tigris.[10] The Bengal, Caspian and Siberian tigers, and lion rank among the biggest cats.[11][12][13]
It is the national animal of both India and Bangladesh.[14]

Characteristics

The Bengal tiger's coat is yellow to light orange, with stripes ranging from dark brown to black; the belly and the interior parts of the limbs are white, and the tail is orange with black rings. The white tiger is a recessive mutant of the Bengal tiger, which is reported in the wild from time to time in Assam, Bengal, Bihar and especially from the former State of Rewa. However, it is not to be mistaken as an occurrence of albinism. In fact, there is only one fully authenticated case of a true albino tiger, and none of black tigers, with the possible exception of one dead specimen examined in Chittagong in 1846.[15]
Male Bengal tigers have an average total length of 270 to 310 cm (110 to 120 in) including the tail, while females measure 240 to 265 cm (94 to 104 in) on average.[12] The tail is typically 85 to 110 cm (33 to 43 in) long, and on average, tigers are 90 to 110 cm (35 to 43 in) in height at the shoulders.[16] The weight of males ranges from 180 to 258 kg (397 to 569 lb), while that of the females ranges from 100 to 160 kg (220 to 350 lb).[12] The smallest recorded weights for Bengal tigers are from the Bangladesh Sundarbans, where adult females are 75 to 80 kg (165 to 176 lb).[17] Bengal tigers have exceptionally stout teeth, and the canines are the longest among all living felids; measuring from 7.5 to 10 cm (3.0 to 3.9 in) in length.[18]
Genetic ancestry[edit]
Bengal tigers are defined by three distinct mitochondrial nucleotide sites and 12 unique microsatellite alleles. The pattern of genetic variation in the Bengal tiger corresponds to the premise that they arrived in India approximately 12,000 years ago.[19] This is consistent with the lack of tiger fossils from the Indian subcontinent prior to the late Pleistocene and the absence of tigers from Sri Lanka, which was separated from the subcontinent by rising sea levels in the early Holocene.[20]
Body weight[edit]

Female Bengal tiger in the Ranthambhore Tiger Reserve
Bengal tigers may weigh up to 325 kg (717 lb) and reach a head and body length of 320 cm (130 in).[18] Several scientists indicated that adult male Bengal tigers from Nepal, Bhutan, and Assam, Uttarakhand and West Bengal in northern India (collectively, the tigers of the Terai) consistently attain more than 227 kg (500 lb) of body weight. Seven adult males captured in Chitwan National Park in the early 1970s had an average weight of 235 kg (518 lb) ranging from 200 to 261 kg (441 to 575 lb), and that of the females was 140 kg (310 lb) ranging from 116 to 164 kg (256 to 362 lb).[21] Males from northern India are nearly as large as Siberian tigers with a greatest length of skull of 332 to 376 mm (13.1 to 14.8 in).[22]
Verifiable Sundarbans tiger weights are not found in any scientific literature. Forest Department records list weight measurements for these tigers, but none are verifiable and all are guesstimates. There are also reports of head and body lengths, some of which are listed as over 365.7 cm (144.0 in). More recently, researchers from the University of Minnesota and the Bangladesh Forest Department carried out a study for the US Fish and Wildlife Service and weighed three Sundarbans tigresses from Bangladesh. All three tigers were female, two of which were collared, captured and sedated, but the other one had been killed by local villagers. The two collared tigresses were weighed using 150 kg (330 lb) scales, and the tigress killed by villagers was weighed using a balance scale and weights. The two collared females both showed signs of teeth wear and both were between 12 and 14 years old. The tigress killed by the villagers was a young adult, probably between 3 and 4 years old, and she was likely a pre-territorial transient. The three tigresses had a mean weight of 76.7 kg (169 lb). One of the two older female's weight 75 kg (165 lb) weighed slightly less than the mean because of her old age and relatively poor condition at the time of capture. Skulls and body weights of Sundarbans tigers were found to be distinct from other subspecies, indicating that they may have adapted to the unique conditions of the mangrove habitat. Their small sizes are probably due to a combination of intense intraspecific competition and small size of prey available to tigers in the Sundarbans, compared to the larger deer and other prey available to tigers in other parts.[23]


The South Asian river dolphin has the long, pointed nose characteristic of all river dolphins. Its teeth are visible in both the upper and lower jaws even when the mouth is closed. The teeth of young animals are almost an inch long, thin and curved; however, as animals age, the teeth undergo considerable changes and in mature adults become square, bony, flat disks. The snout thickens towards its end. The species does not have a crystalline eye lens, rendering it effectively blind, although it may still be able to detect the intensity and direction of light. Navigation and hunting are carried out using echolocation.[11] They are unique among cetaceans in that they swim on their sides.[12] The body is a brownish color and stocky at the middle. The species has only a small, triangular lump in the place of a dorsal fin. The flippers and tail are thin and large in relation to the body size, which is about 2-2.2 meters in males and 2.4-2.6 m in females. The oldest recorded animal was a 28-year-old male, 199 cm in length.[13] Mature females are larger than males. Sexual dimorphism is expressed after females reach about 150 cm (59 in); the female rostrum continues to grow after the male rostrum stops growing, eventually reaching approximately 20 cm (7.9 in) longer.

Distribution and habitat[edit]
The South Asian river dolphins are native to the freshwater river systems located in Nepal, India, Bangladesh, and Pakistan.[1] They can be most commonly found in water with high abundance of prey and reduced flow.[11]
The Ganges subspecies (P. g. gangetica) can be found along the Ganges-Brahmaputra-Meghna and Karnaphuli-Sangu river systems of Bangladesh and India, although its range formerly extended to Nepal.[11] A small subpopulation can be still found on the Ghaghara River and possibly the Sapta Kosi River. The majority of the Indus subspecies (P. g. minor) is located between the Sukkur and Guddu barrage in the Sind Province of Pakistan.[11] Two smaller subpopulations have also been recorded in the Punjab and Khyber Pakhtunkhwa Provinces. Since the two river systems are not connected in any way, it is a mystery how these sub-species ended up in the two rivers. It is improbable that the river dolphins made it from one river to another through the sea route since the two estuaries are very far apart. According to Land of the Seven Rivers: A Brief History of India's Geography by Sanjeev Sanyal, a likely explanation is that several north Indian rivers like the Sutlej and Yamuna changed their path in ancient times and carried the dolphins with them.[14]

<div style="background-color:pink;padding:5px;border-radius:5px;overflow:hidden"><div style="background-color:orange;padding:5px;border-radius:5px;overflow:hidden"></div><textarea maxlength="50" rows="7" cols="50" style="position:relative;max-width:100%">The South Asian river dolphin (Platanista gangetica) is a freshwater or river dolphin found in India, Bangladesh, Nepal and Pakistan which is split into two subspecies, the Ganges river dolphin (P. g. gangetica) and Indus river dolphin (P. g. minor).[2] The Ganges river dolphin is primarily found in the Ganges and Brahmaputra Rivers and their tributaries in Bangladesh, India and Nepal, while the Indus river dolphin is found in the Indus River in Pakistan and its Beas and Sutlej tributaries. From the 1970s until 1998, they were regarded as separate species; however, in 1998, their classification was changed from two separate species to subspecies of a single species (see taxonomy below).

The Ganges river dolphin has been recognized by the government of India as its National Aquatic Animal.[3] The Indus river dolphin has been named as the National Mammal of Pakistan.[4] Further, the Gangetic river dolphin has been elected to be the city animal of the Indian city of Guwahati.[5]

Taxonomy[edit]
The species was described by two separate authors, Lebeck and Roxburgh, in 1801, and it is unclear to whom the original description should be ascribed.[6] Until the 1970s, the South Asian river dolphin was regarded as a single species. The two subspecies are geographically separate and have not interbred for many hundreds if not thousands of years. Based on differences in skull structure, vertebrae and lipid composition scientists declared the two populations as separate species in the early 1970s.[7] In 1998, the results of these studies were questioned and the classification reverted to the pre-1970 consensus of a single species containing two subspecies until the taxonomy could be resolved using modern techniques such as molecular sequencing. The latest analyses of mitochondrial DNA of the two subspecies did not display the variances needed to support their classification as separate species.[8] Thus, at present, this one species with two subspecies is recognized in the genus Platanista, the P. g. gangetica (Ganges river dolphin) and the P. g. minor (Indus river dolphin).[9]
Synonyms
1.blind river dolphin, side-swimming dolphin
2.Ganges subspecies: Gangetic dolphin, Ganges susu,[10] shushuk
3.Indus subspecies: bhulan, Indus dolphin, Indus blind dolphin


Snow leopards are slightly smaller than the other big cats but, like them, exhibit a range of sizes, generally weighing between 27 and 55 kg (60 and 121 lb), with an occasional large male reaching 75 kg (165 lb) and small female of under 25 kg (55 lb).[19][20] They have a relatively short body, measuring in length from the head to the base of the tail 75 to 150 cm (30 to 60 in).[21] However, the tail is quite long, at 80 to 100 cm (31 to 39 in), with only the domestic-cat-sized marbled cat being relatively longer-tailed.[22][23] They are stocky and short-legged big cats, standing about 60 cm (24 in) at the shoulder.[24]
Snow leopards have long, thick fur, and their base color varies from smoky gray to yellowish tan, with whitish underparts. They have dark grey to black open rosettes on their bodies, with small spots of the same color on their heads and larger spots on their legs and tails. Unusually among cats, their eyes are pale green or grey in color.[22][23]
Snow leopards show several adaptations for living in a cold, mountainous environment. Their bodies are stocky, their fur is thick, and their ears are small and rounded, all of which help to minimize heat loss. Their paws are wide, which distributes their weight better for walking on snow, and have fur on their undersides to increase their grip on steep and unstable surfaces; it also helps to minimize heat loss. Snow leopards' tails are long and flexible, helping them to maintain their balance, which is very important in the rocky terrain they inhabit. Their tails are also very thick due to fat storage and are very thickly covered with fur, which allows them to be used like a blanket to protect their faces when asleep.[23][25]
The snow leopard has a short muzzle and domed forehead, containing unusually large nasal cavities that help the animal breathe the thin, cold air of their mountainous environment.[22]

<div style="background-color:magenta;padding:5px;border-radius:5px;overflow:hidden"><div style="background-color:orange;padding:5px;border-radius:5px;overflow:hidden"></div><textarea maxlength="50" rows="7" cols="50" style="position:relative;max-width:100%">This article is about the cat. For other uses, see Snow Leopard (disambiguation).

The snow leopard or ounce (Panthera uncia syn. Uncia uncia) is a large cat native to the mountain ranges of Central and South Asia. It is listed as Endangered on the IUCN Red List of Threatened Species because, as of 2003, the size of the global wild population was estimated at 4,080–6,590 adults. Fewer than 2,500 individuals may be reproducing in the wild.[1] As of 2016, estimates for the size of the global population vary from at least 4,080 to about 8,700 individuals.[3]
Snow leopards inhabit alpine and subalpine zones at elevations from 3,000 to 4,500 m (9,800 to 14,800 ft). In the northern range countries, they also occur at lower elevations.[4]
Taxonomically, the snow leopard has been classified as Uncia uncia since the early 1930s.[2] Based on genotyping studies, the cat has been considered a member of the genus Panthera since 2008.[1][5] Two subspecies have been attributed, but genetic differences between the two have not been settled.[1]

The snow leopard is also known in its native lands as "wāwrīn pṛāng" (Pashto: واورين پړانګ‎), "shan" (Ladakhi), "zigsa" (Tibetan), "irves" (Mongolian: ирвэс), "bars" or "barys" (Kazakh: барыс [ˈbɑrəs]), "ilbirs" (Kyrgyz: Илбирс), "barfānī chītā" Urdu: برفانی چیتا) and "him tendua" (Sanskrit, Hindi: हिम तेन्दुआ).
According to the American Heritage Dictionary, the origin of the word panthera is unknown. A folk etymology derives the word from the Greek πάν pan ("all") and thēr (beast of prey) because they can hunt and kill almost anything. It was proposed to have come ultimately into Greek from a Sanskrit word meaning "the yellowish animal" or "whitish-yellow". The Greek word πάνθηρ, pánthēr, referred to all spotted felines generically.

The species was first described by the German naturalist Johann Christian Daniel von Schreber on the basis of an illustration in his 1777 publication Die Säugethiere in Abbildungen nach der Natur mit Beschreibungen. Schreber named the cat Felis uncia and gave its type locality as Barbary, Persia, East India, and China.[8] In 1854, the British zoologist John Edward Gray proposed the genus Uncia, to which he subordinated the snow leopard under the name Uncia irbis.[9] British zoologist Reginald Innes Pocock corroborated this classification, but attributed the scientific name Uncia uncia. He also described morphological differences between snow leopards and the then-accepted members of Panthera.[10]
The snow leopard is part of the Panthera lineage, one of the eight lineages of Felidae.

